CREATE TABLE personal (
    id SERIAL PRIMARY KEY,
    nombre VARCHAR NOT NULL,
    rol VARCHAR NOT NULL,
    supervisor INT REFERENCES personal,
    equipo INT GENERATED BY DEFAULT AS IDENTITY,
    CONSTRAINT rolvalid CHECK (
        rol IN ('Medico Senior', 'Medico Junior', 'Tecnico', 'Fisioterapeuta')
    )
);

CREATE OR REPLACE FUNCTION addpersonal()
RETURNS TRIGGER AS $$
DECLARE
    cosas VARCHAR;
    team INT;
BEGIN
    IF NEW.rol = 'Medico Senior' THEN
        NEW.supervisor = NULL;
    ELSIF NEW.rol = 'Medico Junior' THEN
        SELECT p.rol INTO cosas
        FROM personal p
        WHERE p.id = NEW.supervisor;

        IF cosas <> 'Medico Senior' OR cosas IS NULL THEN
            RAISE EXCEPTION 'BLOQUEO: Supervisor invalido ingresado'
                USING ERRCODE = 'P0001';
        END IF;
    ELSIF NEW.rol IN ('Tecnico', 'Fisioterapeuta') THEN
        SELECT p.rol, p.equipo INTO cosas, team
        FROM personal p
        WHERE p.id = NEW.supervisor;

        IF cosas NOT IN ('Medico Senior', 'Medico Junior') OR cosas IS NULL THEN
            RAISE EXCEPTION 'BLOQUEO: Supervisor invalido ingresado'
                USING ERRCODE = 'P0001';
        END IF;

        NEW.equipo = team;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trig1personal
BEFORE INSERT ON personal
FOR EACH ROW
EXECUTE FUNCTION addpersonal();

